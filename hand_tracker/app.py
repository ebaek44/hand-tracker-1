# helper fnction to see how long it takes
def total_dx(points, n=16):
    """
    Returns (dx, run_len) where run_len is the length of the trailing
    contiguous run of non-zero points, capped at n.
    dx is last.x - first.x over that trailing run.
    """
    seq = list(points)
    run = []
    for p in reversed(seq):
        if p and p != [0, 0]:
            run.append(p)
            if len(run) >= n:
                break
        elif run:
            # we already started a run, stop when we hit the first zero
            break
    run = list(reversed(run))
    if len(run) < 2:
        return 0.0, len(run)
    return float(run[-1][0] - run[0][0]), len(run)

# main loop for tracking the hand
def hand_tracker(gesture_data: GestureHandler):
    # Argument parsing #################################################################
    args = get_args()

    # opens camera
    cap_device = args.device
    cap_width = args.width
    cap_height = args.height

    use_static_image_mode = args.use_static_image_mode
    min_detection_confidence = args.min_detection_confidence
    min_tracking_confidence = args.min_tracking_confidence

    use_brect = True

    # Camera preparation ###############################################################
    cap = cv.VideoCapture(cap_device)
    if not cap.isOpened():
        raise RuntimeError(f"Could not open camera device {cap_device}")
    cap.set(cv.CAP_PROP_FRAME_WIDTH, cap_width)
    cap.set(cv.CAP_PROP_FRAME_HEIGHT, cap_height)

    # Model load #############################################################
    mp_hands = mp.solutions.hands
    hands = mp_hands.Hands(
        static_image_mode=use_static_image_mode,
        max_num_hands=1,
        min_detection_confidence=min_detection_confidence,
        min_tracking_confidence=min_tracking_confidence,
    )

    keypoint_classifier = KeyPointClassifier()

    point_history_classifier = PointHistoryClassifier()

    # Warm-up the model to avoid first-frame latency
    try:
        dummy_kp = [0.0] * 42        # 21 keypoints * 2
        _ = keypoint_classifier(dummy_kp)

        dummy_hist = [0.0] * 32      # history_length(16) * 2
        _ = point_history_classifier(dummy_hist)
    except Exception as e:
        print("Warm-up failed (not fatal):", e)

    # Read labels ###########################################################
    with resources.files("hand_tracker.model.keypoint_classifier") \
            .joinpath("keypoint_classifier_label.csv") \
            .open("r", encoding="utf-8-sig") as f:
        keypoint_classifier_labels = [row[0] for row in csv.reader(f)]

    with resources.files("hand_tracker.model.point_history_classifier") \
            .joinpath("point_history_classifier_label.csv") \
            .open("r", encoding="utf-8-sig") as f:
        point_history_classifier_labels = [row[0] for row in csv.reader(f)]

    # FPS Measurement ########################################################
    cvFpsCalc = CvFpsCalc(buffer_len=10)

    # Coordinate history #################################################################
    history_length = 16
    point_history = deque(maxlen=history_length)

    # Finger gesture history ################################################
    finger_gesture_history = deque(maxlen=history_length)

    #  ########################################################################
    mode = 0
    current_gesture_id = None
    gesture_start_time = None
    gesture_duration = 0
    
    # for stability of the finger movement
    stable_fg_id = 0
    fg_candidate_id = 0
    prev_fg_candidate_id = -1
    streak = 0
    K = 4
    D_MIN_PX = max(int(0.12 * cap_width), 60)
    lock_dir = 0 # last confirmed direction
    lock_until = 0.0 # timestamp until opposite flips are ignored
    LOCKOUT_SECS = 2.0 # change this for how long you want lockout

    frame_i = 0
    # main loop for the camera
    while True:
        fps = cvFpsCalc.get()

        # Process Key (ESC: end) #################################################
        key = cv.waitKey(1)
        if key == 27:  # ESC
            break
        number, mode = select_mode(key, mode)

        # Camera capture #####################################################
        ret, image = cap.read()
        if not ret:
            break
        
        frame_i += 1
        # this will skip frames to help with latency (you can tweak if even more)
        if frame_i % 2 == 1:
            continue
        
        image = cv.flip(image, 1)  # Mirror display
        debug_image = copy.deepcopy(image)

        # Detection implementation #############################################################
        image = cv.cvtColor(image, cv.COLOR_BGR2RGB)

        image.flags.writeable = False
        results = hands.process(image)
        image.flags.writeable = True

        #  ####################################################################
        if results.multi_hand_landmarks is not None:
            for hand_landmarks, handedness in zip(results.multi_hand_landmarks,
                                                  results.multi_handedness):
                # Bounding box calculation
                brect = calc_bounding_rect(debug_image, hand_landmarks)

                # calculate the minimum pixel travel by the 
                bx1, by1, bx2, by2 = brect
                box_w = max(1, bx2 - bx1)
                # move this up if too sensitive and down if not sensitive enough
                D_MIN_PX = max(int(0.30 * box_w), 20)
                # Landmark calculation
                landmark_list = calc_landmark_list(debug_image, hand_landmarks)

                # Conversion to relative coordinates / normalized coordinates
                pre_processed_landmark_list = pre_process_landmark(
                    landmark_list)
                pre_processed_point_history_list = pre_process_point_history(
                    debug_image, point_history)
                
                # Write to the dataset file
                logging_csv(number, mode, pre_processed_landmark_list,
                            pre_processed_point_history_list)

                # Hand sign classification
                hand_sign_id = keypoint_classifier(pre_processed_landmark_list)
                if hand_sign_id == 2:  # Point gesture
                    point_history.append(landmark_list[8])
                else:
                    point_history.append([0, 0])

                # Finger gesture classification
                finger_gesture_id = 0
                point_history_len = len(pre_processed_point_history_list)
                if point_history_len == (history_length * 2):
                    finger_gesture_id = point_history_classifier(
                        pre_processed_point_history_list
                    )

                fg_candidate_id = finger_gesture_id
                # ---- Lockout check ----
                now = time.time()
                if now < lock_until:
                    if (fg_candidate_id == 1 and lock_dir == -1) or (fg_candidate_id == 2 and lock_dir == +1):
                        # Opposite direction during lock → override to STOP
                        fg_candidate_id = 0  
                
                # --- Consecutive-frame stabilization ---

                # has to be pointer hand_sign
                is_pointing = (hand_sign_id == 2)
                moving_horiz = (fg_candidate_id in (1, 2))
                if not is_pointing:
                    fg_candidate_id = 0
                
                dx, run_len = total_dx(point_history, n = history_length)
                has_run = run_len >= 4
                has_disp = abs(dx) >= D_MIN_PX and has_run

                cand_bin = 1 if (is_pointing and moving_horiz) else 0

                if fg_candidate_id == prev_fg_candidate_id:
                    streak = min(streak + 1, 10_000)
                else:
                    streak = 1
                prev_fg_candidate_id = cand_bin
                                
                # determine direction purely from dx sign (screen coords after cv.flip: dx>0 = right)
                dir_sign = 0
                if has_disp:
                    if dx > 0:
                        dir_sign = +1   # right
                    elif dx < 0:
                        dir_sign = -1   # left
            
                if not is_pointing:
                    stable_fg_id = 0
                    lock_dir = 0
                    lock_until = 0.0
                    streak = 0

                else:
                    # confirm swipe when pointing, moving horizontally, streak >= K, and we have a direction
                    if cand_bin == 1 and dir_sign != 0 and streak >= K:
                        stable_fg_id = 1 if dir_sign == +1 else 2  # 1=right, 2=left (UI-friendly mapping)
                        lock_dir = dir_sign
                        lock_until = now + LOCKOUT_SECS
                
                final_fg_id = stable_fg_id
            
                # Update Gesture ids
                candidate_id = hand_sign_id

                # Update gesture duration
                if current_gesture_id != candidate_id:
                    current_gesture_id = candidate_id
                    gesture_start_time = time.time()
                    gesture_duration = 0.0
                else:
                    gesture_duration = time.time() - gesture_start_time
                
                # Update the shared gesture duration
                gesture_data.update_gesture(hand_sign_id, gesture_duration, final_fg_id)
                gesture_data.process_gesture()

                
                # Drawing part
                debug_image = draw_bounding_rect(use_brect, debug_image, brect)
                debug_image = draw_landmarks(debug_image, landmark_list)
                debug_image = draw_info_text(
                    debug_image,
                    brect,
                    handedness,
                    keypoint_classifier_labels[hand_sign_id],
                    point_history_classifier_labels[final_fg_id],
                )

        else:
            point_history.append([0, 0])
            finger_gesture_history.clear()
            pointer_id = 0

        debug_image = draw_point_history(debug_image, point_history)
        debug_image = draw_info(debug_image, fps, mode, number)

        # Screen reflection #############################################################
        cv.imshow('Hand Gesture Recognition', debug_image)

    cap.release()
    cv.destroyAllWindows()


# maps keyboard input to logging mode
def select_mode(key, mode):
    number = -1
    if 48 <= key <= 57:  # 0 ~ 9
        number = key - 48
    if key == 110:  # n
        mode = 0
    if key == 107:  # k
        mode = 1
    if key == 104:  # h
        mode = 2
    return number, mode

# calculates a bound rectangle with mediapipe landmarks in image pixels
def calc_bounding_rect(image, landmarks):
    image_width, image_height = image.shape[1], image.shape[0]

    landmark_array = np.empty((0, 2), int)

    for _, landmark in enumerate(landmarks.landmark):
        landmark_x = min(int(landmark.x * image_width), image_width - 1)
        landmark_y = min(int(landmark.y * image_height), image_height - 1)

        landmark_point = [np.array((landmark_x, landmark_y))]

        landmark_array = np.append(landmark_array, landmark_point, axis=0)

    x, y, w, h = cv.boundingRect(landmark_array)

    return [x, y, x + w, y + h]

# converts mediapipes normalized landmarks to pixel coordinates
def calc_landmark_list(image, landmarks):
    image_width, image_height = image.shape[1], image.shape[0]

    landmark_point = []

    # Keypoint
    for _, landmark in enumerate(landmarks.landmark):
        landmark_x = min(int(landmark.x * image_width), image_width - 1)
        landmark_y = min(int(landmark.y * image_height), image_height - 1)
        # landmark_z = landmark.z

        landmark_point.append([landmark_x, landmark_y])

    return landmark_point

# Prepares a feature vector
def pre_process_landmark(landmark_list):
    temp_landmark_list = copy.deepcopy(landmark_list)

    # Convert to relative coordinates
    base_x, base_y = 0, 0
    for index, landmark_point in enumerate(temp_landmark_list):
        if index == 0:
            base_x, base_y = landmark_point[0], landmark_point[1]

        temp_landmark_list[index][0] = temp_landmark_list[index][0] - base_x
        temp_landmark_list[index][1] = temp_landmark_list[index][1] - base_y

    # Convert to a one-dimensional list
    temp_landmark_list = list(
        itertools.chain.from_iterable(temp_landmark_list))

    # Normalization
    max_value = max(list(map(abs, temp_landmark_list)))

    def normalize_(n):
        return n / max_value

    temp_landmark_list = list(map(normalize_, temp_landmark_list))

    return temp_landmark_list

# builds a sequence feature
def pre_process_point_history(image, point_history):
    image_width, image_height = image.shape[1], image.shape[0]

    temp_point_history = copy.deepcopy(point_history)

    # Convert to relative coordinates
    base_x, base_y = 0, 0
    for index, point in enumerate(temp_point_history):
        if index == 0:
            base_x, base_y = point[0], point[1]

        temp_point_history[index][0] = (temp_point_history[index][0] -
                                        base_x) / image_width
        temp_point_history[index][1] = (temp_point_history[index][1] -
                                        base_y) / image_height

    # Convert to a one-dimensional list
    temp_point_history = list(
        itertools.chain.from_iterable(temp_point_history))

    return temp_point_history

